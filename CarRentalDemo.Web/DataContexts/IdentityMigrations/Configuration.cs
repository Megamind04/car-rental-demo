    internal sealed class Configuration : DbMigrationsConfiguration<CarRentalDemo.Web.DataContexts.IdentityDb>
    {
        public Configuration()
        {
            AutomaticMigrationsEnabled = false;
            MigrationsDirectory = @"DataContexts\IdentityMigrations";
        }

        protected override void Seed(CarRentalDemo.Web.DataContexts.IdentityDb context)
